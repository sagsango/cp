int binpow(int a,ll p){
	int r = 1;
	while( p ){
		if( p & 1 )
			r = mul(r,a);
		a = mul(a,a), p >>= 1;
	}
	return r;
}
class Solution {
	public:

		typedef long long ll;

		ll mod = 1e9+7;

		ll binpow(ll a,ll p){
			ll r = 1;
			a %= mod;
			while( p ){
				if( p & 1 ){
					r = r * a % mod;
				}
				a = a * a % mod;
				p >>= 1;
			}
			return r;
		}
		int minNonZeroProduct(int p) {

			/*
			// 0 -> 4
			// 1 -> 4
			// 2 -> 4

			// x * x * x * x * x * x * x
			2.  2.  2.  2
			// 1.  1.  1.  1
			4.  4.  4.  4
			 */

			long long a = (1ll<<p)-1ll;
			long long b = a^1;

			return a  % mod * binpow(b,(1ll<<(p-1))-1) % mod;






		}
};
