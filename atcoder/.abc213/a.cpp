const int mod = 1e9+7;
int add(int x,int y){
	int z = x + y;
	if( z >= mod ){
		z -= mod;
	}
	return z;
}
int sub(int x,int y){
	int z = x - y;
	if( z < 0 ){
		z += mod;
	}
	return z;
}
int mul(int x,int y){
	ll z = 1ll * x * y;
	if( z >= mod ){
		z %= mod;
	}
	return z;
}
int binpow(int a,ll p){
	int r = 1;
	while( p ){
		if( p & 1 )
			r = mul(r,a);
		a = mul(a,a), p >>= 1;
	}
	return r;
}

vector<int>bitCount(int n){
	n += 1;
	vector<int>cnt(30);
	for(int i=0;i<30;++i){
		int x = 1<<(i+1);
		int y = 1<<(i+0);

		cnt[i] = ( n/x ) * y + max(0,(n%x) - y);
	}
	return cnt;
}

vector<int>Count(int n){
	vector<int>cnt(30);
	for(int i=1;i<=n;++i){
		for(int j=0;j<30;++j){
			cnt[j] += (i>>j) &1;
		}
	}
	return cnt;
}

